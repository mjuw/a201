        <p style="text-indent: 50px;"><h3>Swedish Massage Therapy</h3>
			<ul>
				<li>This is the most common type of massage therapy in the United States. It is also known as Swedish massage or simply massage therapy.</li>
				<li>Massage therapists use long smooth strokes, kneading, and circular movements on superficial layers of muscle using massage lotion or oil.</li>
				<li>Swedish massage therapy can be very gentle and relaxing.</li>
				<li>If you've never had massage before, this is a good one to try first.</li>
		</p>

		<p style="text-indent: 50px;"><h3>Aromatherapy Massage</h3>
			<br/>Aromatherapy massage is massage therapy with the addition of one or more scented plant oils called essential oils to address specific needs.
			<br/>The massage therapist can select oils that are relaxing, energizing, stress-reducing, balancing, etc. One of the most common essential oils used in aromatherapy massage is lavender.
			<br/>Aromatherapy massage is particularly suited to stress-related conditions or conditions with an emotional component.
			<br/>Find out more about hot stone massage.
		</p>
		
		<p style="text-indent: 50px;"><h3>Hot Stone Massage</h3>
			<br/>Heated, smooth stones are placed on certain points on the body to warm and loosen tight muscles and balance energy centers in the body.
			<br/>The massage therapist may also hold stones and apply gentle pressure with them. The warmth is comforting. Hot stone massage is good for people who have muscle tension but prefer lighter massage.
		</p>
		
		<p style="text-indent: 50px;"><h3>Deep Tissue Massage</h3>
			<br/>Deep tissue massage targets the deeper layers of muscle and connective tissue. The massage therapist uses slower strokes or friction techniques across the grain of the muscle.
			<br/>Deep tissue massage is used for chronically tight or painful muscles, repetitive strain, postural problems, or recovery from injury.
			<br/>People often feel sore for one to two days after deep tissue massage. Learn more about this style of massage: Deep Tissue Massage.
		</p>
		
		
		<p style="text-indent: 50px;"><h3>Shiatsu</h3>
			<br/>Shiatsu is a form of Japanese bodywork that uses localized finger pressure in a rhythmic sequence on acupuncture meridians.
			<br/>Each point is held for for two to eight seconds to improve the flow of energy and help the body regain balance.
			<br/>People are normally pleasantly surprised when they try shiatsu for the first time. It is relaxing yet the pressure is firm, and there is usually no soreness afterwards. Find out more about shiatsu.
		</p>


		<p style="text-indent: 50px;"><h3>Thai Massage</h3>
			<br/>Like shiatsu, Thai massage aligns the energies of the body using gentle pressure on specific points. Thai massage also includes compressions and stretches.
			<br/>You don't just lie there--the therapist moves and stretches you into a sequence of postures. It's like yoga without doing any work. Thai massage is more energizing than other forms of massage. It is also reduces stress and improves flexibility and range of motion.
			<br/>Find out more about thai massage. Also, see photos of a Thai massage session.
		</p>
	
		<p style="text-indent: 50px;"><h3>Pregnancy Massage</h3>
			<ul>
				<li>Also called prenatal massage, pregnancy massage is becoming increasingly popular with expectant mothers. Massage therapists who are certified in pregnancy massage know the proper way to position and support the woman's body during the massage, and how to modify techniques.</li>
				<li>Pregnancy massage is used to reduce stress, decrease swelling, relieve aches and pains, and reduce anxiety and depression. The massage is customized to a woman's individual needs. Learn more about pregnancy massage.</li>
		</p>
	
	
		<p style="text-indent: 50px;"><h3>Reflexology</h3>
			<ul>
				<li>Although reflexology is sometimes called foot massage, it is more than simple foot massage. Reflexology involves applying pressure to certain points on the foot that correspond to organs and systems in the body. Reflexology is very relaxing, especially for people who stand on their feet all day or just have tired, achy feet. Learn more about reflexology.</li>
		</p>
	

		<p style="text-indent: 50px;"><h3>Sports Massage</h3>
			<ul>
				<li>Sports massage is specifically designed for people who are involved in physical activity. But you don't have to be a professional athlete to have one-it's also used by people who are active and work out often. The focus isn't on relaxation but on preventing and treating injury and enhancing athletic performance.</li>
				<li>A combination of techniques are used. The strokes are generally faster than Swedish massage. Facilitated stretching is a common technique. It helps to loosen muscles and increase flexibility.</li>
		</p>
	
		<p style="text-indent: 50px;"><h3>Back Massage</h3>
			<ul>
				<li>Some massage clinics and spas offer 30-minute back massages. If a back massage is not expressly advertised, you can also book a 30- or 40-minute massage and ask that the massage therapist to focus on your back.</li>
		</p>


		<p style="text-indent: 50px;"><h3>Bowen Massage</h3>
			<ul>
				<li>The Bowen Technique uses a series of simple, gentle moves across muscle and connective tissue.</li>
				<li>It is similar to tuning a stringed instrument that sends harmonic vibrations that balance the body.</li>
				<li>Because it works in harmony with the body, the Bowen Technique is incredibly effective for any muscular, skeletal, or nerve imbalance.</li>
				<li>It has also been successful in treating chronic pain due to injury.</li>
				<li>The technique is so gentle it can be used on anyone from infants to the elderly.</li>
				<li>It produces a deep relaxation and can release blocked emotional energy, which can accelerate the healing process.</li>
		</p>
	
